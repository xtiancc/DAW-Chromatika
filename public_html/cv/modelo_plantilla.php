<?php 	

require('../fpdf/fpdf.php');

class PDF extends FPDF {

	// Función para crear una cabecera personalizada
 	function Header() {

		// Imagen con el logo de nuestro sitio, al pulsar en el nos lleva a nuestro sitio web
 		$this -> Image('../img/logo-negativo.png', 10, 7, 50, 0, '', 'www.https://cromatikaproyectodaw.000webhostapp.com/');
		$this -> Ln(20);
	}

	// Función para crear un pie de página personalizado
 	function Footer() {

		// Nos despegamos del borde inferior para que el texto sea visible
		$this -> SetY(-15);
		 
		// Seleccionamos el tipo de fuente y añadimos el número de página centrado
 		$this -> setFont('Arial', 'I', 10);
 		$this -> cell(0, 10, UTF8('Página '.$this->PageNo().'/{nb}'), 0, 0, 'C');

	 }
	 
	 // Función para crear marco sobre imagen usuario
	 function RoundedRect($x, $y, $w, $h, $r, $corners = '1234', $style = '') {
	     
        $k = $this->k;
        $hp = $this->h;
        if($style=='F')
            $op='f';
        elseif($style=='FD' || $style=='DF')
            $op='B';
        else
            $op='S';
        $MyArc = 4/3 * (sqrt(2) - 1);
        $this->_out(sprintf('%.2F %.2F m',($x+$r)*$k,($hp-$y)*$k ));

        $xc = $x+$w-$r;
        $yc = $y+$r;
        $this->_out(sprintf('%.2F %.2F l', $xc*$k,($hp-$y)*$k ));
        if (strpos($corners, '2')===false)
            $this->_out(sprintf('%.2F %.2F l', ($x+$w)*$k,($hp-$y)*$k ));
        else
            $this->_Arc($xc + $r*$MyArc, $yc - $r, $xc + $r, $yc - $r*$MyArc, $xc + $r, $yc);

        $xc = $x+$w-$r;
        $yc = $y+$h-$r;
        $this->_out(sprintf('%.2F %.2F l',($x+$w)*$k,($hp-$yc)*$k));
        if (strpos($corners, '3')===false)
            $this->_out(sprintf('%.2F %.2F l',($x+$w)*$k,($hp-($y+$h))*$k));
        else
            $this->_Arc($xc + $r, $yc + $r*$MyArc, $xc + $r*$MyArc, $yc + $r, $xc, $yc + $r);

        $xc = $x+$r;
        $yc = $y+$h-$r;
        $this->_out(sprintf('%.2F %.2F l',$xc*$k,($hp-($y+$h))*$k));
        if (strpos($corners, '4')===false)
            $this->_out(sprintf('%.2F %.2F l',($x)*$k,($hp-($y+$h))*$k));
        else
            $this->_Arc($xc - $r*$MyArc, $yc + $r, $xc - $r, $yc + $r*$MyArc, $xc - $r, $yc);

        $xc = $x+$r ;
        $yc = $y+$r;
        $this->_out(sprintf('%.2F %.2F l',($x)*$k,($hp-$yc)*$k ));
        if (strpos($corners, '1')===false)
        {
            $this->_out(sprintf('%.2F %.2F l',($x)*$k,($hp-$y)*$k ));
            $this->_out(sprintf('%.2F %.2F l',($x+$r)*$k,($hp-$y)*$k ));
        }
        else
            $this->_Arc($xc - $r, $yc - $r*$MyArc, $xc - $r*$MyArc, $yc - $r, $xc, $yc - $r);
        $this->_out($op);
        
    }

    function _Arc($x1, $y1, $x2, $y2, $x3, $y3) {
        
        $h = $this->h;
        $this->_out(sprintf('%.2F %.2F %.2F %.2F %.2F %.2F c ', $x1*$this->k, ($h-$y1)*$this->k,
            $x2*$this->k, ($h-$y2)*$this->k, $x3*$this->k, ($h-$y3)*$this->k));
            
    }

}

function UTF8($texto) {

	return iconv('UTF-8', 'ISO-8859-1//TRANSLIT', $texto);
	
}

